                <p>Golconda fort is listed as an archaeological treasure on the official "List of Monuments" prepared by
                    the
                    Archaeological Survey of India under The Ancient Monuments and Archaeological Sites and Remains
                    Act.[14]
                    Golconda consists of four distinct forts with a 10 km (6.2 mi) long outer wall with 87 semicircular
                    bastions (some still mounted with cannons), eight gateways, and four drawbridges, with a number of
                    royal
                    apartments and halls, temples, mosques, magazines, stables, etc. inside. The lowest of these is the
                    outermost enclosure entered by the "Fateh Darwaza" (Victory gate, so called after Aurangzeb’s
                    triumphant
                    army marched in through this gate) studded with giant iron spikes (to prevent elephants from
                    battering
                    them down) near the south-eastern corner. An acoustic effect can be experienced at Fateh Darwazaan,
                    a
                    hand clap at a certain point below the dome at the entrance reverberates and can be heard clearly at
                    the
                    'Bala Hisar' pavilion, the highest point almost a kilometer away. This worked as a warning in case
                    of an
                    attack.</p>
