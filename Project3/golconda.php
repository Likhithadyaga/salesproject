                <p>The Golconda fort used to have a vault where the famous Koh-i-Noor and Hope diamonds were once stored
                    along with other diamonds.[12] Golconda is renowned for the diamonds found on the south-east at
                    Kollur Mine near Kollur, Guntur district, Paritala and Atkur in Krishna district and cut in the city
                    during the Kakatiya reign. At that time, India had the only known diamond mines in the world.
                    Golconda was the market city of the diamond trade, and gems sold there came from a number of mines.
                    The fortress-city within the walls was famous for diamond trade.[citation needed] Its name has taken
                    a generic meaning and has come to be associated with great wealth. Some gemologists use this
