        <h1>About Golconda Fort</h1>

        <br>
        <p>Golconda Fort, originating as a modest mud fort constructed in the 11th century by Pratāparudra of
            the Kakatiya Empire, transformed into a formidable citadel under Sultan Quli of the Qutb Shahi
            Empire by 1518, designated as the capital of the Golconda Sultanate. Following its transfer to the
            Bahmani kings via a sanad from the Rajah of Warangal, Golconda prospered under their administration.
            Sultan Quli's declaration of independence in 1518 marked the inception of the Qutb Shahi dynasty,
            during which the fort underwent significant expansion over 62 years. Golconda thrived as a hub for
            cotton weaving during the early 17th century, exporting premium textiles globally. However, in 1687,
            after enduring an eight-month siege, the fort succumbed to Mughal Emperor Aurangzeb, spelling the
            end of the Qutb Shahi rule and resulting in the capture of the final Golconda king, Abul Hassan Tana
            Shah.</p>
        <br>
