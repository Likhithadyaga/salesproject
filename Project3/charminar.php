            <div class="col-md-8">
                <p>Muhammad Quli Qutb Shah, the fifth ruler of the Qutb Shahi dynasty, commissioned the construction of
                    the Charminar in 1591 to mark the eradication of plague and celebrate the beginning of the second
                    Islamic millennium year (1000 AH). It was built at the heart of the newly established city of
                    Hyderabad, replacing Golconda as the capital. The monument, completed in 1592 at a cost of Rs. 9
                    lakhs, symbolizes the city's prosperity and cultural vibrancy.
                    Constructed at the crossroads of the historical trade route linking Hyderabad to international
                    markets via Machilipatnam, the Charminar served as the centerpiece of the Old City. Its strategic
                    location facilitated trade and commerce, shaping the city's layout into quadrants and chambers
                    around it. 
                    Despite various theories regarding its purpose, the Charminar stands as a testament to Hyderabad's
                    rich history and the vision of its founder. While folklore suggests a romantic origin tied to Qutb
                    Shah's love story, historians largely dismiss it. Nevertheless, the legend remains popular among
                    locals, adding to the monument's allure. </p>
