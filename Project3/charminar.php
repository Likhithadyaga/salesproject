        <h1>About Charminar</h1>

        <br>
        <p>The Charminar, meaning 'four minarets,' is a historic monument in Hyderabad, Telangana, India, dating back to
            1591. It holds cultural and religious significance with a mosque atop for over 425 years. Surrounding it are
            bustling local markets, adding to its charm and drawing tourists. Adjacent to the Makkah Masjid, it hosts
            various festivals like Eid-ul-adha and Eid al-Fitr. Positioned on the Musi River's east bank, it is flanked
            by the Laad Bazaar and the ornate Makkah Masjid. Recognized as an archaeological and architectural treasure
            by the Archaeological Survey of India, its English name reflects its designâ€”ornate minarets supported by
            four grand arches. Charminar stands as a symbol of Hyderabad's rich history and cultural heritage, welcoming
            visitors from around the world.</p>
